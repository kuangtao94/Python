# ----------------------------------------
# 逻辑运算符：and、or、not
# ----------------------------------------
a = True
b = False
# and 与运算，当所有条件都为True时才返回True，否则返回False
if a and b:
    print("变量a和b都为True")
else:
    print("变量a和b其中一个为False，或者都为False")

# or 或运算，只要满足其中一个条件为True就返回True，否则返回False
if a or b:
    print("变量a和b其中一个为True，或者都为True")
else:
    print("变量a和b都为False")

# not 非运算，当变量为True时返回False，变量为False时返回True
if not a:
    print("变量a为False")
else:
    print("变量a为True")
