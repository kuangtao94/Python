# ----------------------------------------
# 身份运算符：is、is not
# ----------------------------------------
a = 'Python2'
b = 'Python3'
# is 判断两个标识符是不是引用自同一个对象。如果引用的是同一个对象则返回True，否则返回False
if a is b:
    print("a和b有相同的标识")
else:
    print("a和b没有相同的标识")
# is not 判断两个标识符是不是引用自不同的对象。如果引用的不是同一个对象则返回True，否则返回False
if a is not b:
    print("a和b没有相同的标识")
else:
    print("a和b有相同的标识")
