# ----------------------------------------
# 算术运算符：+、-、*、/、%、**、//
# ----------------------------------------
a = 10
b = 3
# + 加，两个对象相加
c = a + b
print("a + b =", c)
# - 减，取负数或两个对象相减
c = -a
print("-a =", c)
c = a - b
print("a - b =", c)
# * 乘，两个数相乘或返回一个被重复若干次的字符串
c = a * b
print("a * b =", c)
c = 'abc' * 3
print("'abc' * 3 =", c)
# / 除，返回两个数相除的结果，得到浮点数
c = a / b
print("a / b =", c)
# % 取模，返回两个数相除的余数
c = a % b
print("a % b =", c)
# ** 幂，a**b表示返回a的b次幂
c = a ** b
print("a ** b =", c)
# // 取整除，返回两个数相除后商的整数部分
c = a // b
print("a // b =", c)
