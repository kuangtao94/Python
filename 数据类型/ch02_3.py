# ----------------------------------------
# 成员运算符：in、not in
# ----------------------------------------
a = '荷花'
b = '黄花'
flowers = ['桂花', '菊花', '玫瑰', '牡丹', '荷花']
# in 如果指定的元素在后面的序列中则返回True，否则返回False
if a in flowers:
    print("变量a在列表flowers中")
else:
    print("变量a不在列表flowers中")
# not in 如果指定的元素不在后面的序列中则返回True，否则返回False
if b not in flowers:
    print("变量b不在列表flowers中")
else:
    print("变量b在列表flowers中")
